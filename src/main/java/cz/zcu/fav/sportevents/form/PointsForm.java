package cz.zcu.fav.sportevents.form;

public class PointsForm {

    Integer teamId;
    Integer newPoints;
    Integer newBonus;
    Integer points;
    Integer bonus;

    public Integer getTeamId() {
        return teamId;
    }

    public void setTeamId(Integer teamId) {
        this.teamId = teamId;
    }

    public Integer getNewPoints() {
        return newPoints;
    }

    public void setNewPoints(Integer newPoints) {
        this.newPoints = newPoints;
    }

    public Integer getNewBonus() {
        return newBonus;
    }

    public void setNewBonus(Integer newBonus) {
        this.newBonus = newBonus;
    }

    public Integer getPoints() {
        return points;
    }

    public void setPoints(Integer points) {
        this.points = points;
    }

    public Integer getBonus() {
        return bonus;
    }

    public void setBonus(Integer bonus) {
        this.bonus = bonus;
    }
}
